create table if not exists bid_result_fetch_results (
    id bigint generated by default as identity primary key,
    source varchar(200) not null,
    tender_id bigint,
    project_id bigint,
    project_name varchar(500) not null,
    result varchar(20) not null,
    amount numeric(19,2),
    fetch_time timestamp(6) not null,
    status varchar(20) not null,
    ignored_reason varchar(255),
    confirmed_at timestamp(6),
    confirmed_by bigint,
    created_at timestamp(6) not null,
    updated_at timestamp(6) not null
);

create index if not exists idx_bid_result_fetch_status
    on bid_result_fetch_results (status);

create index if not exists idx_bid_result_fetch_project
    on bid_result_fetch_results (project_id);

create index if not exists idx_bid_result_fetch_tender
    on bid_result_fetch_results (tender_id);

create table if not exists bid_result_reminders (
    id bigint generated by default as identity primary key,
    project_id bigint not null,
    project_name varchar(500) not null,
    owner_id bigint,
    owner_name varchar(255) not null,
    reminder_type varchar(30) not null,
    status varchar(20) not null,
    remind_time timestamp(6) not null,
    last_reminder_comment varchar(500),
    last_result_id bigint,
    created_by bigint,
    created_by_name varchar(255) not null,
    created_at timestamp(6) not null,
    updated_at timestamp(6) not null
);

create index if not exists idx_bid_result_reminder_project
    on bid_result_reminders (project_id);

create index if not exists idx_bid_result_reminder_status
    on bid_result_reminders (status);

create table if not exists bid_result_sync_logs (
    id bigint generated by default as identity primary key,
    operation_type varchar(20) not null,
    source varchar(200) not null,
    message varchar(500),
    affected_count integer not null,
    operator_id bigint,
    operator_name varchar(255) not null,
    created_at timestamp(6) not null
);

create index if not exists idx_bid_result_sync_type
    on bid_result_sync_logs (operation_type);
